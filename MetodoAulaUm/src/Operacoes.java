
public class Operacoes {

    public static void soma (double numeroUm, double numeroDois){
        double somaResultado = numeroUm + numeroDois;
        System.out.println("A soma de " + numeroUm + " + " + numeroDois + "é " + somaResultado);

    }

    public static void subtracao (double numeroUm, double numeroDois){
        double subtracaoResultado = numeroUm - numeroDois;
        System.out.println("A subtracao de " + numeroUm + " + " + numeroDois + "é " + subtracaoResultado);

    }

    public static void divisao (double numeroUm, double numeroDois){
        double divisaoResultado = numeroUm / numeroDois;
        System.out.println("A divisão de " + numeroUm + " + " + numeroDois + "é " + divisaoResultado);

    }

    public static void multiplicacao (double numeroUm, double numeroDois){
        double multiplicacaoResultado = numeroUm * numeroDois;
        System.out.println("A multiplicação de " + numeroUm + " + " + numeroDois + "é " + multiplicacaoResultado);

    }
}
